package mirrg.boron.peg.ilmenite;

public class IlmeniteRuntimeException extends Exception
{

	public IlmeniteRuntimeException()
	{
		super();
	}

	public IlmeniteRuntimeException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace)
	{
		super(message, cause, enableSuppression, writableStackTrace);
	}

	public IlmeniteRuntimeException(String message, Throwable cause)
	{
		super(message, cause);
	}

	public IlmeniteRuntimeException(String message)
	{
		super(message);
	}

	public IlmeniteRuntimeException(Throwable cause)
	{
		super(cause);
	}

}
